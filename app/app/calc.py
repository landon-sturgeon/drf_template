def add(x: int, y: int) -> int:
    """Add two numbers together.

    :param x: first number to add
    :param y: second number to add
    :return: sum of first number and second number
    """
    return x + y


def subtract(x: int, y: int) -> int:
    """Subtract two numbers from each other.

    :param x: first number to be reduced
    :param y: number to reduce the first term by
    :return: subtraction of second term from first term
    """
    return x - y
